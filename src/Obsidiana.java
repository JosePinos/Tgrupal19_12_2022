public class Obsidiana extends Cubito {
    private String color;
    private double dureza;
    public Obsidiana(){
        super();
        this.color="Morado";
        this.dureza=12.5;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public double getDureza() {
        return dureza;
    }

    public void setDureza(double dureza) {
        this.dureza = dureza;
    }
}
